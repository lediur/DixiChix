#import "UIColor+Extras.h"

@implementation UIColor (Extras)

+ (UIColor *)dcPink {
    return [UIColor colorWithRed:255/255.0f green:0/255.0f blue:255/255.0f alpha:1];
}

+ (UIColor *)dcRed {
    return [UIColor colorWithRed:255/255.0f green:64/255.0f blue:85/255.0f alpha:1];
}

@end
